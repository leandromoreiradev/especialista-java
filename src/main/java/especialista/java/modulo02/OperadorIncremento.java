package especialista.java.modulo02;

public class OperadorIncremento {
    public static void main(String[] args) {
        int num1 = 10;
        System.out.println("VALOR ANTES DA ALTERAÇÃO:" + num1);
        System.out.println("ANTES DE ACRESCENTAR num++:" + num1++);
        System.out.println("DEPOIS DE ACRESCENTAR num:" + num1);

        int num2 = 20;
        System.out.println("VALOR ANTES DA ALTERAÇÃO:" + num2);
        System.out.println("ACRECENTANDO ANTES ++num:" + ++num2);
        System.out.println("ACRECENTANDO ANTES ++num:" + ++num2);




    }
}
